package Model;

public class departement {

	private int id;
	private String dp_nom;
	private String etat;
	public String getEtat() {
		return etat;
	}
	public void setEtat(String etat) {
		this.etat = etat;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDp_nom() {
		return dp_nom;
	}
	public void setDp_nom(String dp_nom) {
		this.dp_nom = dp_nom;
	}
	
	
	
}
